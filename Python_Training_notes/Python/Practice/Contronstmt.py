# for

for x in range(10):
    print(x)

for x in range(2):
    print(x)
    continue

for x in range(2):
    print(x)
    break

# While

k=0
while (k<5):
    print(k)
    k+=1

# if-elif-else

x=int(input('Enter a number :   '))

if (x>=90):
    print('Grade A')
elif (x>=80):
    print('Grade B')
elif (x>=70):
    print('Grade C')
else:
    print('Grade D')



# if-else

x=int(input('Enter a number :   '))

if (x%2 is 0):
    print('Even number')
else:
    print('Odd number')
